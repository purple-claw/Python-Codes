# Examples of bool data type

# True and False are the two possible values of bool data type
is_true = True
is_false = False

# bool values can be used in conditional statements
if is_true:
    print("This statement is true.")
else:
    print("This statement is false.")

# bool values can be used in logical operations
result = is_true and is_false
print("The result of the logical operation is:", result)

# bool values can be used in comparisons
greater_than = 10 > 5
print("Is 10 greater than 5?", greater_than)

less_than = 5 < 10
print("Is 5 less than 10?", less_than)